package day06;
//유자차 클래스
public class Yuja {
	private int yuja;
	private int sugar;
	
	//setter, getter
	public void setYuja(int yuja) {
		this.yuja = yuja;
	}
	public void setSugar(int sugar) {
		this.sugar = sugar;
	}
	
	public int getYuja() {
		return yuja;
	}
	public int getSugar() {
		return sugar;
	}
}
